# ~ Вывод сообщения на экран
print("Hello World!")

# ~ Вывод значения переменной на экран
print_text = "Hello World!"
print(print_text)
# ~ Сложение двух строковых переменных и вывод на экран
# ~ user_name="Андрей"
print_text = "Привет, " + user_name + "!"
print(print_text)

# ~ Операции над целочисленными переменными
x=6
y=3
print(x + y)
print(x - y)
print(x * y)
# ~ Операции над вещественными переменными
pi=3.142
e=2.718
print(pi + e)
print(pi - e)
print(pi * e)
# ~ Логические операции
x=True  
y=False 
print(x or y)   # if x == true then result=y else x
print(x and y)  # if x == false then result=x else y
# ~ Ввод значения переменной с клавиатуры
user_name = input("Как Вас зовут? ")
